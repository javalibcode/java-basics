public class AssignmentOperatorExample2{
	
	public static void main(String[] args){
		// Assignment Operators : &=, |=, ^=, <<=, >>=, >>>=
		
		int a = 7; 
		
		a&=2; // a = a&2 = 7 &2 
		System.out.println(a);
		
		a|=2; // a = a |2 = 2 | 2
		System.out.println(a);
		
		a^=2; 
		System.out.println(a);
		
		a<<=2;
		System.out.println(a);
		
		a>>=2;
		System.out.println(a);
		
		a>>>=2;
		System.out.println(a);
		
		
	}
	

}