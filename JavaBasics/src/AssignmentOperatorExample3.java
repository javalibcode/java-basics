public class AssignmentOperatorExample3{
	
	public static void main(String[] args){
		// Assignment Operators : =, +=, -=, *=, /=, %=
		
		int a = 7; 
		a+=5; // a = a+5
		System.out.print(a+" ");
		a-=5; // a = a-5
		System.out.print(a+" ");
		a*=5; // a = a*5
		System.out.print(a+" ");
		a/=5; // a = a/5
		System.out.print(a+" ");
		a%=5; // a = a%5
		System.out.print(a+" ");
		
	}
	

}