public class BitwiseOperatorExample{
	
	public static void main(String[] args){
		// Bitwise Operators : &, |, ^
		
		int a1 = 2; 
		int b1 = 4; 
		
		System.out.println(a1 & b1); //0
		System.out.println(a1 | b1); //6
		System.out.println(a1 ^ b1); //6 
		
		
		boolean a2 = true; 
		boolean b2 = false; 
		
		System.out.println(a2 & b2); //0
		System.out.println(a2 | b2); //6
		System.out.println(a2 ^ b2); //6 
		
		
		
	}
	

}