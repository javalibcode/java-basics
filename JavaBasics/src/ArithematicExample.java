public class ArithematicExample{
	
	public static void main(String[] args){
		// Arithematic Operators : +,-,*,/,%
		
		int a = 7; 
		int b = -5; 
		
		System.out.println(a+b); //12
		System.out.println(a-b); //2
		System.out.println(a*b); //35
		System.out.println(a/b); //1
		System.out.println(a%b); //2
		
	}
	

}