public class ShiftOperatorExample{
	
	public static void main(String[] args){
		// Shift Operators : <<, >>, >>>
		
		int a = -7; 
		
		System.out.println(a << 1);
		System.out.println(a << 2);
		
		System.out.println(a >> 1);
		System.out.println(a >> 2);
		
		System.out.println(a >>> 1);
		System.out.println(a >>> 2);
		
	}
	

}